import Foundation
import SwiftUI

enum AppLanguage: String, CaseIterable, Identifiable {
    case english = "en"
    case chinese = "zh-Hans"

    var id: String { rawValue }

    var locale: Locale {
        Locale(identifier: rawValue)
    }
}

final class LanguageManager: ObservableObject {
    @Published var language: AppLanguage {
        didSet {
            UserDefaults.standard.set(language.rawValue, forKey: Self.storageKey)
        }
    }

    static let storageKey = "eastwood.language"

    init() {
        if let saved = UserDefaults.standard.string(forKey: Self.storageKey),
           let language = AppLanguage(rawValue: saved) {
            self.language = language
        } else if let preferred = Locale.preferredLanguages.first, preferred.hasPrefix("zh") {
            self.language = .chinese
        } else {
            self.language = .english
        }
    }

    func text(_ key: String) -> String {
        switch language {
        case .english:
            return Self.english[key] ?? key
        case .chinese:
            return Self.chinese[key] ?? Self.english[key] ?? key
        }
    }

    func format(_ key: String, _ args: CVarArg...) -> String {
        String(format: text(key), locale: language.locale, arguments: args)
    }

