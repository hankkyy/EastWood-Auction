enum AppErrorPresenter {
    private static var languageManager: LanguageManager { LanguageManager() }

    static func text(_ key: String) -> String {
        languageManager.text(key)
    }

    static func format(_ key: String, _ args: CVarArg...) -> String {
        String(format: text(key), locale: languageManager.language.locale, arguments: args)
    }

    static func message(for error: Error) -> String {
        if let apiError = error as? APIClientError {
            switch apiError {
            case .badURL:
                return text("error.invalidURL")
            case .unauthorized:
                return text("error.unauthorized")
            case .network:
                return text("error.network")
            case .server(let message):
                return message
            }
        }

        if let urlError = error as? URLError {
            switch urlError.code {
            case .timedOut:
                return text("error.timeout")
            case .notConnectedToInternet, .networkConnectionLost, .internationalRoamingOff, .dataNotAllowed:
                return text("error.offline")
            case .userAuthenticationRequired:
                return text("error.authentication")
            case .badURL, .unsupportedURL:
                return text("error.invalidURL")
            case .badServerResponse, .cannotParseResponse:
                return text("error.server")
            default:
                return urlError.localizedDescription
            }
        }

        let nsError = error as NSError
        if nsError.domain == NSURLErrorDomain {
            return message(for: URLError(.init(rawValue: nsError.code)))
        }
        return error.localizedDescription
    }
}
